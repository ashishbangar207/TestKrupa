package data_type_pgm;

public class Data_Type_Pgm {
	public static void main(String arrs[]) {
		String s="Ashish";
		System.out.println(s);
		
	//String is a non-primitive data types used to stored the information into the sequence 
		//character into the double quoet
		
		boolean a;
		a= true;
		System.out.println(a);
		
		byte q =10;
		System.out.println(q);
		
		short t=25000;
		System.out.println(t);
		
		int h=43500000;
		System.out.println(h);
		
		long k=1000000000000l;
		System.out.println(k);
		
		double f=134.80f;
		System.out.println(f);
		
		float x=23454.09f;
		System.out.println(x);
		char j='o';
		System.out.println(j);
		
		char v='*';
		System.out.println(v);
		
		
		
		
		
		
	}

}
